/**
 * Get translation for a section
 * @param {string} section - Section name (e.g., 'sidebar', 'dashboard')
 * @param {string} lang - Language code ('en' or 'de')
 * @returns {object} Translation object for the section
 */
export function getTranslation(section, lang = 'en') {
  const sectionData = translations[section];
  if (!sectionData) {
    console.warn(`Translation section "${section}" not found`);
    return {};
  }
  return sectionData[lang] || sectionData['en'] || {};
}

